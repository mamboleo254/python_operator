#Arithmetic operators
num1 =100
num2 =20
print("num1+num2=",num1+num2) #additional operator
print("num1-num2=",num1-num2) #substraction operator
print("num1*num2=",num1*num2) #multiplication operator
print("num1/num2=",num1/num2) #divisional operator
print("2^3=",2**3)            #exponential operator
print("22//7=",22//7)          #float division
